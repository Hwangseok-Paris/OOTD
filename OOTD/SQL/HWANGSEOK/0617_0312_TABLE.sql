  CREATE OR REPLACE TRIGGER "OOTD"."TRG_PRODUCT_STOCK" 
AFTER INSERT
ON O_ORDER_LIST
FOR EACH ROW
    BEGIN
        IF :NEW.ORDER_SIZE = 'S'
        THEN 
            UPDATE PRODUCT
            SET PRODUCT_STOCK_S = PRODUCT_STOCK_S - :NEW.ORDER_QUANTITY
            WHERE PRODUCT_NO = :NEW.PRODUCT_NO;
        END IF;
        IF :NEW.ORDER_SIZE = 'M' THEN 
            UPDATE PRODUCT
            SET PRODUCT_STOCK_M = PRODUCT_STOCK_M - :NEW.ORDER_QUANTITY
            WHERE PRODUCT_NO = :NEW.PRODUCT_NO;
        END IF;
        IF :NEW.ORDER_SIZE = 'L' THEN 
            UPDATE PRODUCT
            SET PRODUCT_STOCK_L = PRODUCT_STOCK_L - :NEW.ORDER_QUANTITY
            WHERE PRODUCT_NO = :NEW.PRODUCT_NO;
        END IF;
    END;


